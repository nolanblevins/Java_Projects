
public class CalcBackEnd 
{
	// adds 2 values and returns result
	public double add(double a, double b)
	{
		double c = a + b;
		return c;
	}
	// multiplies 2 values and returns result
	public double multiply(double a, double b)
	{
		double c = a * b;
		return c;
	}
	// divides first value by second value and returns result
	public double divide(double a, double b)
	{
		double c = a/b;
		return c;
	}
	// subtracts second value from first value and returns result
	public double subtract(double a, double b)
	{
		double c = a - b;
		return c;
	}
	
	

}
